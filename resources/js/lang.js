function getPreferredLang() {
    // Priority: URL param 'lang' -> localStorage -> navigator.language -> 'en'
    const urlLang = new URLSearchParams(window.location.search).get('lang');
    if (urlLang && langData[urlLang]) return urlLang;
    const stored = localStorage.getItem('vfu_lang');
    if (stored && langData[stored]) return stored;
    const nav = navigator.language ? navigator.language.split('-')[0] : null;
    if (nav && langData[nav]) return nav;
    return 'en';
}

function applyLang(lang) {
    const data = langData[lang] || langData.en;
    // title
    if (data.title) document.title = data.title;
    // html lang attribute
    document.documentElement.lang = lang;
    // elements with data-i18n
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (!key) return;
        const text = data[key];
        if (typeof text === 'undefined') return;
        if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
            el.placeholder = text;
        } else if (el.tagName === 'IMG' && el.hasAttribute('data-i18n-alt')) {
            el.alt = text;
        } else {
            el.textContent = text;
        }
    });
    // save preference
    localStorage.setItem('vfu_lang', lang);
}

// Expose a simple switcher API
window.VFULang = {
    init() {
        const lang = getPreferredLang();
        applyLang(lang);
        // observe URL changes (history pushState)
        window.addEventListener('popstate', () => {
            const l = getPreferredLang();
            applyLang(l);
        });
        // handle custom events
        window.addEventListener('vfu:langchange', e => {
            const newLang = (e.detail && e.detail.lang) ? e.detail.lang : getPreferredLang();
            applyLang(newLang);
        });
    },
    set(lang) {
        if (!langData[lang]) return;
        const u = new URL(window.location);
        u.searchParams.set('lang', lang);
        history.pushState({}, '', u.toString());
        applyLang(lang);
    }
};

document.addEventListener('DOMContentLoaded', () => {
    window.VFULang.init();
});